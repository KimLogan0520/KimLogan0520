# 정수 나누기 연산자 : //
print("3 / 2 =", 3 / 2)  # 3 / 2 = 1.5
print("3 // 2 =", 3 // 2)  # 3 // 2 = 1 ( 정수 나누기 연산자를 사용하면 .5는 버림처리된다. )

# 나머지 연산자 : %
print("3 % 2 =", 3 % 2)  # 몫: 1, 나머지: 1

# 제곱연산자 : **
print("10 ** 2 =", 10 ** 2)  # 10의 제곱이므로 100

print(2 * 2 / 2 * 2)  # 4.0 ( 결과가 정수가 아니라 실수임 )

print("3462를 17로 나누었을때 몫과 나머지?")
print("- 몫 :", 3462 // 17)
print("- 나머지 :", 3462 % 17)